    private void preencherComboProdutos() {
        listaProdutos = HibernateUtil.getSession().createCriteria(Produto.class).list();
        comboProduto.removeAllItems();
        for (Produto p : listaProdutos) {
            comboProduto.addItem(p.getNome());
        }
    }
    
    private void preencherComboPessoas() {
        listaPessoas = HibernateUtil.getSession().createCriteria(Pessoa.class).list();
        comboPessoa.removeAllItems();
        for (Pessoa p : listaPessoas) {
            comboPessoa.addItem(p.getNome());
        }
    }
    
