    private void btnADDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnADDActionPerformed
         if (validaSalvar()) {
                try {
                    
                    
                    if (!campoID.getText().equals("")) {
                        pedido.setId(Integer.parseInt(campoID.getText()));
                    } else {
                        pedido.setId(null);
                    }
                    
                    pedido.setPessoa(listaPessoas.get(comboPessoa.getSelectedIndex()));
                    for(ItemPedido i : pedido.getItens()){
                        i.setPedido(pedido);
                    }
                    
                    HibernateUtil.beginTransaction();
                    HibernateUtil.getSession().merge(pedido);
                    HibernateUtil.commitTransaction();
                    HibernateUtil.closeSession();
                    
                    limpaCampos();
                    atualizaTabela();
                    
                } catch (Exception ex) {
                    Logger.getLogger(MovimentoPedido.class.getName()).log(Level.SEVERE, null, ex);
                }
        }
    }//GEN-LAST:event_btnADDActionPerformed

    
   
    
